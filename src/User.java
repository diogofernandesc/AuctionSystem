public class User {

    String givenName;
    String familyName;
    String userID;
    String password;

    public User(String givenName, String familyName, String userID, String password) {
        this.givenName = givenName;
        this.familyName = familyName;
        this.userID = userID;
        this.password = password;
    }
}
